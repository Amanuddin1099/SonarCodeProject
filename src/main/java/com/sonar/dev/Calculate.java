package com.sonar.dev;

public class Calculate {
    public int addition(int a, int b) {
        return a + b;
    }

    public int subtraction(int c, int d) {
        return c - d;
    }

    public int divide(int e, int f) {
        return e / f;
    }

    public int multiply(int g, int h) {
        return g * h;
    }


}
